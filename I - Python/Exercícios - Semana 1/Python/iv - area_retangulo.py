""" 
A imobiliária XYZ vende apenas terrenos 
retangulares. Faça um programa para ler 
as dimensões de um terreno e exibir a 
área do mesmo.

Explicação:

Area = Base x Altura

Logo..
"""

base = float(input("Insira aqui a medida da base do terreno: "))
